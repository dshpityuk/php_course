function recursive($num) {
    // Если параметр $num < 10, продолжаем рекурсию
    if ($num > 10) {
    // Уменьшать значение параметра $num, и его выводим в браузер
        echo ($num--) . "<br>";
        //? Производим рекурсивный вызов ф-ции
        recursive($num);
    }else return;
}
